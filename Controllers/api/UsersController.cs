        // GET: api/Users/5
        [ResponseType(typeof(User))]
        public async Task<IHttpActionResult> GetUser(string id)
        {
            User user = await db.Users.FindAsync(id);
            if (user == null)
            {
                return NotFound();
            }

            return Ok(user);
        }

        // PUT: api/Users/5
        [AllowAnonymous]
        [Route("UpdateProfile")]
        public void Put([FromBody] User user)
        {
            using (FMAccountEntities entities = new FMAccountEntities())
            {
                var email = System.Web.HttpContext.Current.User.Identity.Name;
                var entity = entities.Users.FirstOrDefault(u => u.Email == email || u.UserName == email);
                entity.FName = user.FName;
                entity.LName = user.LName;
                entity.MName = user.MName;
                
                entities.SaveChanges();
            }

        }

